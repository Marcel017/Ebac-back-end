public class CalculadoraMedia {
    public static void main(String[] args) {
        double nota1 = 9.9;
        double nota2 = 8.6;
        double nota3 = 9.0;
        double nota4 = 4.5;

        double media = (nota1 + nota2 + nota3 + nota4) / 4;

        System.out.println("A média final é " + media);
    }
}
